#include "spi.h"


/*	SPI口初始化
 *	这里针是对SPIx的初始化
 */
void SPIx_Init(void)
{
	GPIO_InitTypeDef  GPIO_InitStructure;
	SPI_InitTypeDef  SPI_InitStructure;
	
	/* SPI的IO口和SPI外设打开时钟 */
  RCC_APB2PeriphClockCmd(SPI_RCC_GPIO_SCK, ENABLE );
  RCC_APB1PeriphClockCmd(SPI_RCC_SPIx_SCK, ENABLE);
	
	/* SPI的IO口设置 */
	GPIO_InitStructure.GPIO_Pin = SPI_SCK | SPI_MISO | SPI_MOSI;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_AF_PP;  //PB13/14/15复用推挽输出 
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_Init(SPI_GPIOx, &GPIO_InitStructure);
	
	GPIO_InitStructure.GPIO_Pin = SPI_SC;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP;
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_Init(SPI_GPIOx, &GPIO_InitStructure);
	GPIO_SetBits(SPI_GPIOx,GPIO_Pin_12);

	SPI_InitStructure.SPI_Direction = SPI_Direction_2Lines_FullDuplex;  //设置SPI单向或者双向的数据模式:SPI设置为双线双向全双工
	SPI_InitStructure.SPI_Mode = SPI_Mode_Master;		//设置SPI工作模式:设置为主SPI
	SPI_InitStructure.SPI_DataSize = SPI_DataSize_8b;		//设置SPI的数据大小:SPI发送接收8位帧结构
	SPI_InitStructure.SPI_CPOL = SPI_CPOL_High;		//串行同步时钟的空闲状态为高电平
	SPI_InitStructure.SPI_CPHA = SPI_CPHA_2Edge;	//串行同步时钟的第二个跳变沿（上升或下降）数据被采样
	SPI_InitStructure.SPI_NSS = SPI_NSS_Soft;		//NSS信号由硬件（NSS管脚）还是软件（使用SSI位）管理:内部NSS信号有SSI位控制
	SPI_InitStructure.SPI_BaudRatePrescaler = SPI_BaudRatePrescaler_256;		//定义波特率预分频的值:波特率预分频值为256
	SPI_InitStructure.SPI_FirstBit = SPI_FirstBit_MSB;	//指定数据传输从MSB位还是LSB位开始:数据传输从MSB位开始
	SPI_InitStructure.SPI_CRCPolynomial = 7;	//CRC值计算的多项式
	SPI_Init(SPIx, &SPI_InitStructure);  //根据SPI_InitStruct中指定的参数初始化外设SPIx寄存器
	
	SPI_Cmd(SPIx, ENABLE); //使能SPI外设
	
	SPIx_ReadWriteByte(0xff);//启动传输	
}

/*	SPIx速度设置函数
 *	SPI速度=fAPB1/分频系数
 *	@ref SPI_BaudRate_Prescaler:SPI_BaudRatePrescaler_2~SPI_BaudRatePrescaler_256  
 *	fAPB1时钟一般为36Mhz：
 */
void SPIx_SetSpeed(u8 SPI_BaudRatePrescaler)
{
	SPIx->CR1&=0XFFC7;//位3-5清零，用来设置波特率
	SPIx->CR1|=SPI_BaudRatePrescaler;	//设置SPI速度 
	SPI_Cmd(SPIx,ENABLE); //使能SPI2
} 

/*	SPIx 读写一个字节
 *	TxData:要写入的字节
 *	返回值:读取到的字节
 */
u8 SPIx_ReadWriteByte(u8 TxData)
{		 			 
	while (SPI_I2S_GetFlagStatus(SPIx, SPI_I2S_FLAG_TXE) == RESET);//等待发送区空  
	SPI_I2S_SendData(SPIx, TxData); //通过外设SPIx发送一个byte  数据	
	while (SPI_I2S_GetFlagStatus(SPIx, SPI_I2S_FLAG_RXNE) == RESET); //等待接收完一个byte  
	return SPI_I2S_ReceiveData(SPIx); //返回通过SPIx最近接收的数据			    
}

